    private void cmdSimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cmdSimpanMouseClicked
        // TODO add your handling code here:
        String tKodepmsk=txtKode_pmsk.getText();
        String tNamapmsk=txtNama_pmsk.getText();
        String tKodebrg=txtKode_brg.getText();
        String tNamabrg=txtNama_brg.getText();

        
        try{
            if (edit==true){
                stm.executeUpdate("update pemasok set nm_pmsk='"+tNamapmsk
                        +"',kd_brg='"+tKodebrg+"',nm_brg="+tNamabrg+" where kd_pmsk='" + tKodepmsk + "'");
            }else{
                stm.executeUpdate("INSERT into pemasok VALUES('"+tKodepmsk+"','"+tNamapmsk+"','"+tNamabrg+"',"+tKodebrg+")");
            }
            tbl_pmsk.setModel(new DefaultTableModel(dataTable,header));
            baca_data();
            aktif(false);
            setTombol(true);
        }catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmdSimpanMouseClicked

