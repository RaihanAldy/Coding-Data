        private void baca_data()
    {
        try{
           stm = Con.createStatement(); 
           
           pstat = Con.prepareStatement("select * from pemasok",ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
           RsBrg = pstat.executeQuery();

           ResultSetMetaData meta = RsBrg.getMetaData();
           int col = meta.getColumnCount();
           int baris = 0;

           while(RsBrg.next()) {
               baris = RsBrg.getRow();
           }

           dataTable = new Object[baris][col];
           int x = 0;
           RsBrg.beforeFirst();

           while(RsBrg.next()) {
               dataTable[x][0] = RsBrg.getString("kd_pmsk");
               dataTable[x][1] = RsBrg.getString("nm_pmsk");
               dataTable[x][2] = RsBrg.getString("kd_brg"); 
               dataTable[x][3] = RsBrg.getString("nm_brg");
                
              x++;
           }
           tbl_pmsk.setModel(new DefaultTableModel(dataTable,header));
        }
        catch(SQLException e)
        {
           JOptionPane.showMessageDialog(null, e);
        }
    }

